package JavaBasic.src_uts;

public class TipeData {
    public static void main(String[] args) {
        // Boolean
        boolean benar = true;
        boolean salah = false;

        System.out.println(benar);
        System.out.println(salah);

        // Integer
        byte iniByte = 100; // SIZE 1 Byte
        short iniShort = 1000; // SIZE 2 Byte
        int iniInt = 100000; // SIZE 4 Byte
        long iniLong = 1000000; //SIZE 8 Byte
        long iniLong2 = 1_000_000L;

        // Floating point
        float iniFloat = 10.0f;
        double iniDouble = 1239.32;

        // Char
        char e = 'E';
        char c = 'C';

        System.out.println(e);
        System.out.println(c);

    }
}
